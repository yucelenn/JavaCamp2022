public class Main {

    public static void main(String[] args) {
        
        int i = 1;
        while (i<10) {            
            System.out.println(i);
            i++;  // sonsuz döngü olmaması için işlem yapılmalı.
        }
        System.out.println("while döngüsü bitti.");
        
    }
    
}
